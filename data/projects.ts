// Add a safe getter function
export const getProjects = () => {
  try {
    return projects && Array.isArray(projects) ? projects : []
  } catch (error) {
    console.warn("Error accessing projects:", error)
    return []
  }
}

// Add a function to get a specific project safely
export const getProjectById = (id: string) => {
  try {
    const allProjects = getProjects()
    return allProjects.find((project) => project && project.id === id) || null
  } catch (error) {
    console.warn("Error finding project:", error)
    return null
  }
}

