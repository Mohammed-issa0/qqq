const getProjectImages = (projectId: string): string[] => {
  try {
    const projectImages = PROJECT_IMAGES[projectId as keyof typeof PROJECT_IMAGES]
    if (!projectImages) {
      return ["/placeholder.svg?height=400&width=600&text=مشروع"]
    }

    const realImages = projectImages.real || []
    const placeholderImages = projectImages.placeholder || []

    return realImages.length > 0 ? realImages : placeholderImages
  } catch (error) {
    console.warn(`Error getting images for project ${projectId}:`, error)
    return ["/placeholder.svg?height=400&width=600&text=مشروع"]
  }
}

